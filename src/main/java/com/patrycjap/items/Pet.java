package com.patrycjap.items;

public class Pet {

    private String name;
    private String age;
    private String breed;

    public Pet(String name, String age, String breed) {
        this.name = name;
        this.age = age;
        this.breed = breed;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getBreed() {
        return breed;
    }



}
